<template>
   <div :class="getClasses()">
      <slot />
   </div>
</template>

<script>
   export default {
      name: 'GridItem',
      props: {
         sizes: {
            type: Object,
            required: false
         }
      },
      methods: {
         getClasses () {
            let classes = ['grid__item'];
            const { sizes } = this;

            if (!sizes) {
               return classes;
            }

            ['xs', 'sm', 'md', 'lg', 'xl', 'xxl'].forEach(size => {
               if (sizes.hasOwnProperty(size)) {
                  classes.push(size + '-' + sizes[size]);
               }
            })

            return classes;
         }
      }
   }
</script>

